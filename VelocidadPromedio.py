# Calcula la velocidad promedio de un viaje

tiempo = int(input("Teclea el tiempo de viaje en horas enteras: "))
distancia = int(input("Teclea la distacia del viaje en kilometros enteros: "))

velocidad = distancia / tiempo

print("La velocidad promedio es: ", velocidad, "km/h")